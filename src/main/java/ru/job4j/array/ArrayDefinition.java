package ru.job4j.array;

public class ArrayDefinition {
    public static void main(String[] args) {
        short[] array1 = new short[10];
        System.out.println("Размер массива array1: " + array1.length);
        String[] array2 = new String[100500];
        System.out.println("Размер массива array2: " + array2.length);
        float[] array3 = new float[40];
        System.out.println("Размер массива array3: " + array3.length);
    }
}
